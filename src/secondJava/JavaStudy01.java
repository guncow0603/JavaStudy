package secondJava;

public class JavaStudy01 {
    public static void main(String[] args) {
        // X + Y = Z
        int x = 5;
        int y = 10;

        int z = x + y; // 5(피연산자) +(연산자) 10(피연산자)

        System.out.println(z);

        //산술연산자 : +, -, *, /, %(나머지), <<, >>
        //비교연산자 : >, <, >==, <=, ==, !=
        //논리연산자 : &&, ||, !
        //대입연산자 : =, ++, --
        //기타연산자 : (type), ? :, instance of

    }
}
